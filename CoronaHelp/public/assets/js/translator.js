$(function () {
  $('.translate').click(function () {
    lang = $(this).attr('id');

    $('.lang').each(function (index, item) {
      $(this).text(arrLang[lang][$(this).attr('key')]);
      if ($(this).is("input")) {
        $(this).attr("placeholder", arrLang[lang][$(this).attr('key')]);
      }

    });
  });
});

